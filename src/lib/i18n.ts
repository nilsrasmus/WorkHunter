export type MessageKey = keyof typeof messages.sv;

export function resolveLanguage(value?: string | null): AppLanguage {
  return value === "en" ? "en" : "sv";
}

export function t(lang: AppLanguage, key: MessageKey): string {
  return messages[lang][key] ?? messages.en[key];
}

export function useI18n() {
  const { settings } = useSession();
  const lang = resolveLanguage(settings?.language);
  return useMemo(
    () => ({
      lang,
      t: (key: MessageKey) => t(lang, key),
    }),
    [lang],
  );
}
